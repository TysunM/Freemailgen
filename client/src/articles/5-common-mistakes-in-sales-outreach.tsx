export function FiveCommonMistakes() {
  return (
    <>
      <p>
        You've built your prospect list, you have a great product, but your
        cold email campaigns are falling flat. What gives? Chances are, you're
        making one of these common mistakes. Let's break them down so you can
        avoid them.
      </p>

      <h2>1. The Vague Subject Line</h2>
      <p>
        Subject lines like "Quick Question" or "Checking In" are a one-way
        ticket to the trash folder. Your subject line is the gatekeeper to your
        email. It needs to be specific, relevant, and intriguing.
        <br />
        <strong>Instead, try:</strong> "Idea for [Their Company]'s content strategy" or
        "Question about your [Specific Technology] setup."
      </p>

      <h2>2. It's All About You</h2>
      <p>
        The biggest mistake in cold emailing is making it all about you, your
        company, and your product. The recipient doesn't care about you; they
        care about their own problems.
        <br />
        <strong>Instead, try:</strong> Frame your entire email around them. Start
        with an observation about their company, identify a problem they likely
        have, and then briefly introduce your solution.
      </p>

      <h2>3. The "Wall of Text"</h2>
      <p>
        Long, dense paragraphs are intimidating and instantly signal "sales
        pitch." Your email should be easy to read and digest in 30 seconds or
        less.
        <br />
        <strong>Instead, try:</strong> Use short sentences and paragraphs. Break up
        your text with bullet points or numbered lists.
      </p>

      <h2>4. A Weak or Non-Existent Call-to-Action (CTA)</h2>
      <p>
        Ending your email with a vague "Let me know what you think" is a recipe
        for no response. You need to guide the prospect on what to do next.
        <br />
        <strong>Instead, try:</strong> Be specific. "Are you free for a 15-minute
        call next Tuesday or Thursday afternoon to discuss this further?" is much
        more effective.
      </p>

      <h2>5. Giving Up After One Try</h2>
      <p>
        People are busy. Your first email might have arrived at the wrong time.
        Not following up is leaving money on the table.
        <br />
        <strong>Instead, try:</strong> Implement a follow-up sequence. Send a few
        more emails over the next couple of weeks, each with a slightly different
        angle or piece of valuable content.
      </p>

      <p>
        By avoiding these five mistakes, you'll dramatically increase your
        chances of getting a positive response and starting a valuable
        conversation.
      </p>
    </>
  );
}
